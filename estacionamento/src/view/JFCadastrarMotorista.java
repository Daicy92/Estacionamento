      MotoristaDAO dao = new MotoristaDAO();
      
      m.setNomeCompleto(jTFNomeCompleto.getText());
      m.setGenero(jTFGenero.getText());
      m.setRG(Integer.parseInt(jTFRG.getText()));
      m.setCPF(Integer.parseInt(jTFCPF.getText()));
